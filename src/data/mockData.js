// Helper function to get statistics
export const getStatistics = () => {
  return {
    totalAnimals: mockAnimals.length,
    totalOwners: mockOwners.length,
    totalEvents: mockEvents.length,
    healthyAnimals: mockAnimals.filter(a => a.healthStatus === 'Healthy').length,
    underTreatment: mockAnimals.filter(a => a.healthStatus === 'Under Treatment').length,
    totalInventoryValue: mockInventory.reduce((sum, item) => sum + item.totalValue, 0),
    lowStockItems: mockInventory.filter(item => item.quantity <= item.reorderLevel).length,
    animalsByType: mockAnimals.reduce((acc, animal) => {
      acc[animal.type] = (acc[animal.type] || 0) + 1;
      return acc;
    }, {}),
    recentEvents: mockEvents.slice(0, 5),
    monthlyEventCosts: mockEvents
      .filter(e => e.cost > 0)
      .reduce((sum, e) => sum + e.cost, 0),
  };
};
